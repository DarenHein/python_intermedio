import math

# todo el modulo man nos oferece varios metodos para ayuda con algrebra 

#todo *Seno*
numero = 5
resultado = math.sin(numero) #calcular el seno 
print(resultado)

#todo *cos*
resultado = math.cos(numero) #calcula el coseno 
print(resultado)

# todo *tan* 
resultado = math.tan(resultado) #calcula la tangente 
print(resultado)

# todo * funciones de redondeo y valor absoluto *
#todo osea quita o da decimales 
numero2 = 7.5 
resultado = math.ceil(numero2) #redondea si s 5 lo avienta a 8 
print(resultado) 

resultado = math.floor(numero2) # lo regresa a 7 
print(resultado)

# todo * pi y euler * 
